type TranslationKey = keyof typeof dictionaries.en

type I18nContextValue = {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: TranslationKey | string) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

function detectInitialLocale(): Locale {
  if (typeof window === 'undefined') {
    return 'en'
  }

  const storedLocale = window.localStorage.getItem(localeStorageKey)

  if (storedLocale === 'en' || storedLocale === 'th') {
    return storedLocale
  }

  return window.navigator.language.toLowerCase().startsWith('th') ? 'th' : 'en'
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>('en')
  const [isLocaleLoaded, setIsLocaleLoaded] = useState(false)

  useEffect(() => {
    const initialLocale = detectInitialLocale()
    setLocaleState(initialLocale)
    document.documentElement.lang = initialLocale
    setIsLocaleLoaded(true)
  }, [])

  useEffect(() => {
    if (!isLocaleLoaded) {
      return
    }

    document.documentElement.lang = locale
    window.localStorage.setItem(localeStorageKey, locale)
  }, [isLocaleLoaded, locale])

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale: setLocaleState,
      t: (key) => dictionaries[locale][key as TranslationKey] ?? dictionaries.en[key as TranslationKey] ?? key
    }),
    [locale]
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const value = useContext(I18nContext)

  if (!value) {
    throw new Error('useI18n must be used inside I18nProvider')
  }

  return value
}

export function translateStatusKey(status: string) {
  return `status.${status.toLowerCase()}`
}

export function translateEffectKey(effect: string) {
  return `effect.${effect.toLowerCase()}`
}
